package com.trybe.java.regraprogressao;

/**
 * The type Evaluative activity.
 */
public class EvaluativeActivity {

  /**
   * The Evaluation weight.
   */
  int evaluationWeight;
  /**
   * The Evaluation grade.
   */
  int evaluationGrade;
  /**
   * The Evaluation name.
   */
  String evaluationName;

  /**
   * Gets evaluation grade.
   *
   * @return the evaluation grade
   */
  public int getEvaluationGrade() {
    return evaluationGrade;
  }

  /**
   * Sets evaluation grade.
   *
   * @param evaluationGrade the evaluation grade
   */
  public void setEvaluationGrade(int evaluationGrade) {
    this.evaluationGrade = evaluationGrade;
  }

  /**
   * Gets evaluation weight.
   *
   * @return the evaluation weight
   */
  public int getEvaluationWeight() {
    return evaluationWeight;
  }

  /**
   * Sets evaluation weight.
   *
   * @param evaluationWeight the evaluation weight
   */
  public void setEvaluationWeight(int evaluationWeight) {
    this.evaluationWeight = evaluationWeight;
  }

  /**
   * Gets evaluation name.
   *
   * @return the evaluation name
   */
  public String getEvaluationName() {
    return evaluationName;
  }

  /**
   * Sets evaluation name.
   *
   * @param evaluationName the evaluation name
   */
  public void setEvaluationName(String evaluationName) {
    this.evaluationName = evaluationName;
  }

}
